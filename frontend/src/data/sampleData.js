// Simulated promotion engine results - highest buying probability customers
export const getTopCustomersForProduct = (productId, count = 5) => {
  // Simulate ML model prediction - return customers with high loyalty and matching price sensitivity
  const scored = customers.map((customer) => ({
    ...customer,
    buyingProbability:
      Math.random() * 0.5 +
      customer.loyaltyScore * 0.3 +
      (1 - customer.priceSensitivity) * 0.2,
  }));

  return scored
    .sort((a, b) => b.buyingProbability - a.buyingProbability)
    .slice(0, count)
    .map((c) => ({
      ...c,
      buyingProbability: (c.buyingProbability * 100).toFixed(1),
    }));
};

